package loopPractice;

public class doWhileLoop {
    //Let's Print A to z
    public static void main(String[] args) {


        char ch = 'a';      //initialize the value
        do {  //Execute this block of code first
            System.out.println(ch);

            ch++;
        } //to control loop
        while (ch <= 'z');

    }




}



